//tipos de datos basicos en c++

#include <iostream>
				
using namespace std;

int main (){
	int entero = 15; //es aquel que no tiene ningun tipo de dato decimal.
	float flotante = 10.45;//es aquel que tiene valores decimales.
	double mayor = 16.3456;//al igual que el float tiene valores decimales, la diferencia esta en que este tipo de dato puede contener mayor numero de numeros decimales
	char letra = 'a';//son cualquier caracter sean letras o numeros
	
	cout<<entero<<endl;
	cout<<flotante<<endl;
	cout<<mayor<<endl;
	cout<<letra<<endl;
	
	return 0;
}

